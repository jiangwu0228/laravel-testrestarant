    {

        $book = new Book();
        $book->b_name = request('b_name');
        $book->b_email = request('b_email');
        $book->phone = request('phone');
        $book->data = request('data');
        $book->time = request('time');
        $book->person = request('person');

        $book->save();
        return redirect('/book/view');
    }

